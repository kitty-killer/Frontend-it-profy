  const modal = document.getElementById('movieModal');
  const modalPoster = document.getElementById('modalPoster');
  const modalInfo = document.getElementById('modalInfo');
  const closeModal = document.getElementById('closeModal');
  const favoriteBtn = document.getElementById('favoriteBtn');
  
  closeModal.addEventListener('click', () => {
    modal.style.display = 'none';
  });
  window.addEventListener('click', (e) => {
    if (e.target === modal) {
      modal.style.display = 'none';
    }
  });
  function openModal(movie) {
    modal.style.display = 'flex';
    modalPoster.src = movie.posterUrl;
    modalInfo.innerHTML = `
      <h2>${movie.nameEn || movie.nameRu}</h2>
      <p><strong>Год:</strong> ${movie.year}</p>
      <p><strong>Страна:</strong> ${movie.countries.map(c => c.country).join(', ')}</p>
      <p><strong>Жанры:</strong> ${movie.genres.map(g => g.genre).join(', ')}</p>
      <p><strong>Продолжительность:</strong> ${movie.duration} мин.</p>
      <p><strong>Премьера:</strong> ${movie.premiereRu}</p>
    `;
  
    const favorites = loadFavorites();
    favoriteBtn.classList.toggle('active', favorites.includes(movie.kinopoiskId));
    favoriteBtn.onclick = () => toggleFavorite(movie.kinopoiskId);
  }
  
  function loadFavorites() {
    return JSON.parse(localStorage.getItem('favorites')) || [];
  }
  
  function saveFavorites(favorites) {
    localStorage.setItem('favorites', JSON.stringify(favorites));
  }
  
  function toggleFavorite(movieId) {
    let favorites = loadFavorites();
    if (favorites.includes(movieId)) {
      favorites = favorites.filter(id => id !== movieId);
    } else {
      favorites.push(movieId);
    }
    saveFavorites(favorites);
  favoriteBtn.classList.toggle('active', favorites.includes(movieId));
  favoriteBtn.innerHTML = favoriteBtn.classList.contains('active') ? '❤️' : '🤍'; 
}
  
